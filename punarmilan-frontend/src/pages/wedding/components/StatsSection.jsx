import React, { useState, useEffect } from 'react';
import { motion } from 'framer-motion';

const useCounter = (end, duration = 2000) => {
  const [count, setCount] = useState(0);

  useEffect(() => {
    let startTimestamp = null;
    const step = (timestamp) => {
      if (!startTimestamp) startTimestamp = timestamp;
      const progress = Math.min((timestamp - startTimestamp) / duration, 1);
      setCount(Math.floor(progress * end));
      if (progress < 1) {
        window.requestAnimationFrame(step);
      }
    };
    window.requestAnimationFrame(step);
  }, [end, duration]);

  return count;
};

const StatItem = ({ end, suffix, label, icon }) => {
  const count = useCounter(end);

  return (
    <div className="flex flex-col items-center justify-center p-4 group">
      <div className="w-12 h-12 rounded-full bg-wedding-cream flex items-center justify-center text-wedding-gold mb-3 group-hover:scale-110 transition-transform duration-300">
        {icon}
      </div>
      <h3 className="text-3xl sm:text-4xl lg:text-5xl font-editorial text-wedding-gold-dark mb-2">
        {count.toLocaleString()}{suffix}
      </h3>
      <p className="text-[10px] sm:text-xs text-wedding-light uppercase tracking-widest font-semibold text-center">
        {label}
      </p>
    </div>
  );
};

